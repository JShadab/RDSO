			 <p>Railways were introduced in India in 1853 and as their development progressed through to the twentieth century, 
			 several company managed systems grew up.To enforce standardisation and co-ordination amongst various railway systems, 
			 the Indian Railway Conference Association(IRCA) was set up in 1903, followed by the Central Standards Office (CSO) in 1930, 
			 for preparation of designs, standards and specifications. However, till independence, most of the designs and manufacture of railway 
			 equipments was entrusted to foreign consultants. With Independence and the resultant phenomenal increase in country’s industrial and economic activity, 
			 which increased the demand of rail transportation - a new organisation called Railway Testing and Research Centre (RTRC) was setup in 1952 at Lucknow. 
			 Central Standards Office (CSO) and the Railway Testing and Research Centre (RTRC) were integrated into a single unit named Research Designs and Standards Organisation (RDSO) in 1957, 
			 under Ministry of Railways at Lucknow. The status of RDSO has been changed from an ’Attached Office’ to ’Zonal Railway’ since 01.01.2003.</p>
			
